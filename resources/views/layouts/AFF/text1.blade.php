  <h2 class="">CUM POT AJUNGE PARAȘUTIST CALIFICAT?</h2>    
<p>AFF-ul este metoda ideală de a deveni paraşutist.
 Cursul constă în minim 15 salturi de cădere liberă de la 4000 m şi un salt de tandem la început
  pentru a te familiariza cu mediul. De tine se va ocupă în tot acest timp cel puţin un instructor de AFF pregătit 
  la standarde internaţionale.</p>
  <p>De-a lungul acestui curs vei învăţa să îţi controlezi corpul şi să îţi găseşti stabilitatea
   în cădere liberă iar de aici mai departe vei sări singur sub supravegherea instructorului
    până când îndeplineşti condiţiile prevăzute pentru obţinerea licenţei de paraşutist
     (minim 25 de salturi dintre care 15 de cădere liberă).</p>
  
