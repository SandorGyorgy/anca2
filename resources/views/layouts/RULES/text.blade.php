  <h2 class="">MICILE DETALII FAC DIFERENȚA!</h2>    
Pentru a ne putea bucura la maxim de senzațiile ce ni le oferă acest sport, trebuie să fim extrem de constienți de toate detaliile.
Pentru aceasta, am scris un set de reguli, care cuprinde toate informațiile ce trebuie respectate, de la decolare la aterizare. Chiar dacă
uneori nu convine parașutiștilor mai "rebeli", trebuie să facem o sesizare de fiecare dată, căci de la ceva banal la ceva tragic este un drum scurt!
</div>
<div class="container mt-5">
<h4 class="clickable-heading">DOCUMENTE</h4>

<ul style="display:none">    
<li>Licenţă paraşutist/aprobarea de zbor pentru elevi;</li>
<li>Carnet zbor;</li>
<li>Licenţă medicală;</li>
<li>Act de identitate;</li>
<li>Acte paraşută (în cazul paraşutelor personale): documentul paraşutei cu data plierii paraşutei 
de rezervă, in termen de valabilitate, acte aparat de siguranţă, conform legislatiei in vigoare;</li>
<li>Paraşutiştii vor avea în permanenţă asupra lor toate documentele necesare zborului;</li>
<li>Înainte de zbor/salt, se va lua la cunostinta si semna briefingul de catre toţi participanţii la
 zbor (exceptie facând pasagerii de tandem);</li>
<li>Plata contribuţiei pentru salturi şi plata chiriei echipamentului (daca e cazul) se va face
 înainte de îmbarcarea în avion.</li>
</ul>

<h4 class="clickable-heading">SIGURANȚĂ</h4>

<ul style="display:none">
<li>Aeroclubul poate restricţiona accesul la zbor a paraşutiştilor care au încălcat regulile Asociaţiei sau siguranţa zborului;</li>
<li>Limitele impuse de intensitatea vântului sunt:</li>
<ul style="list-style-type:disc">
<li>7 m/s pentru elevi parașutiști sau pentru parașutiști cu experiență mai mică de 50 salturi;</li>
<li>9 m/s pentru parașutiști cu experiență între 51 și 100 salturi;</li>
<li>11 m/s pentru parașutiști cu experiență între 101 și 500 salturi;</li>
<li>5 m/s pentru parașutiști ce execută salturi pe timp de noapte sau pe apă.</li>
<li>> 12 m/s – va zbura doar personalul angajat şi colaborator cu Aeroclubul Teritorial Baia Mare;</li>
<li>> 14 m/s – zborul va fi oprit;</li>
</ul>
<li>La înregistrare, echipamentul personal va fi controlat de un instructor/ tehnician din cadrul Asociaţiei, inclusiv accesoriile;</li>
<li>Paraşutistul este responsabil pentru controlul echipamentului propriu înainte de fiecare salt. Instructorii asistă elevii în controlul paraşutei acestora;</li>
<li>În cazul în care paraşutistul doreşte să sară cu o paraşuta mai mica decât cea cu care sare în mod uzual, se va adresa unui instructor pentru aprobarea acesteia.</li>
<li>Elevii sub supraveghere indirecta care nu au obţinut încă licenţa A au dreptul să sară doar cu instructori;</li>
</ul>

<h4 class="clickable-heading">ECHIPAMENT</h4>

<ul style="display:none">
<li>Este obligatoriu pentru toţi paraşutişti să aibă echipate sistemele de paraşută cu un sistem de siguranţă funcţional tip AAD (Automation Activation Device). AAD tipul  ”FXC 12000″ sau similar nu este acceptat pentru categoria Freefly. Este obligatoriu ca paraşuta cu care se sare să aibă un AAD pornit, funcţional şi în termen de valabilitate, exceptie pot fi salturile de swoop cu acordul unui instructor;</li>
<li>Pentru Freefly este obligatoriu un sistem de deschidere “BOC” (aşezat în josul containerului);</li>
<li>Clapele de la chingile paraşutei principale să fie bine fixate şi să nu iasa;</li>
<li>Condiţie excelentă a buzunarului din spandex pentru freefly şi în condiţii bune pentru zborul pe burtă;</li>
<li>Pentru freefly recomandăm un ham fix pe corpul paraşutistului. Acesta nu ar trebui să se mişte;</li>
<li>Velcroul de pe ham, de la mânerele de largare şi rezervă să fie în condiţii bune, brida de la paraşuta extractoare bine protejată;</li>
<li>Ocheţii de închidere (de la principală, dar şi de la rezervă) destul de strânşi şi în condiţie bună;</li>
<li>Voalura principală şi mărimea acesteia trebuie să fie în funcţie de nivelul de experienţă.</li>
</ul>

<h4 class="clickable-heading">ÎMBARCAREA</h4>

<ul style="display:none">
<li>Prezentarea la zona de îmbarcare se va face complet echipaţi şi cu minim 5 minute înainte de decolare;</li>
<li>Elevii şi tandemurile urcă primii;</li>
<li>Înainte de îmbarcare se va indica un „jump master” pentru saltul respectiv care va stabili, în functie de dorinţele dumneavoastră şi tipul de salt, ordinea de îmbarcare şi de părăsire a avionului;</li>
<li>Ordinea de părăsirea a aeronavei este stabilită de jump master şi se ghideaza în general după urmatoarele reguli (implicit ordinea de îmbarcare va fi inversă enumerării de mai jos):</li>
<ul style="list-style-type:disc">
<li>cei cu skysurf ies primii, dar nu dacă lansarea se face deasupra oraşului;</li>
<li>belly flyers, de la grupurile cele mai mari la cele mai mici;</li>
<li>free flyers, de la grupurile cele mai mari la cele mai mici;</li>
<li>cei ce deschid sus;</li>
<li>tandemuri şi studenţi;</li>
<li>wingsuits, tracking;</li>
</ul>
<li>În condiţii speciale de vânt această ordine poate fi schimbată de staff-ul DZ-ului;</li>
<li>Îmbarcarea în aeronavă se va face sub îndrumarea jump masterului, respectându-se schema de îmbarcare a aeronavei;</li>
</ul>

<h4 class="clickable-heading">PĂRĂSIREA AERONAVEI</h4>

<ul style="display:none">
<li>Se vor anunta 3 minute până la salt pentru ca toţi paraşutiştii să poată să se pregătească;</li>
<li>Paraşutiştii îşi vor verifica paraşuta (în special extractoarea) înaintea părăsirii aeronavei;</li>
<li>Paraşutiştii vor deschide uşa şi vor începe să sară la comanda pilotului si confirmarea Jump Master-ului, după ce au făcut vizarea în prealabil;</li>
<li>Pentru aeronava Pilatus Porter paraşutiştii nu vor atinge montantul şi roata;</li>
<li>Permisiunea de lansare este data de pilot. Nu acceptăm corectări şi întoarceri. Cu toate acestea paraşutistul este responsabil să vizeze, iar decizia de salt aparţine fiecaruia în parte;</li>
<li>În cazul în care parasiţi avionul la înălţimi mici sau pentru lucru relativ pe cupolă, întrebaţi un instructor al asociaţiei care sunt procedurile speciale care se aplică, înainte de salt;</li>
<li>Se va respecta timpul dintre grupuri la părăsire, luând în considerare şi timpul necesar pentru a părăsi aeronava;</li>
</ul>

<h4 class="clickable-heading">CĂDEREA LIBERĂ</h4>

<ul style="display:none">
<li>Paraşutiştii care vor să sară în formaţie trebuie să deţină o licenţă minim categoria B. Detinatorii unei licente categoria A pot efectua salturi în formaţie doar cu paraşutişti experimentaţi (instructori sau deţinători ai minim categoriei C);</li>
<li>Dacă nu aveţi experienţă suficientă, lucraţi tot timpul la 90 de grade faţă de pistă;</li>
<li>Intr-un salt de tracking se va zbura perpendicular pe pistă (verificaţi cu un instructor direcţia bună pentru track).</li>
</ul>

<h4 class="clickable-heading">SEPARAREA LA DESCHIDERE</h4>

<ul style="display:none">
<li>Înălţimea minima de separare recomandată este: 1700 m pentru grupurile mari si 1500 m pentru grupurile mici (<5 persoane);</li>
<li>Dacă este posibil faceţi tot timpul track 90 de grade faţă de pistă pentru a evita coliziunile cu grupurile dinaintea sau de dupa grupul vostru;</li>
<li>Înălţimea minimă de deschidere e de 1.000m;</li>
<li>Înălţimea minimă de deschidere pentru tandem (instructor+pasager) este de 1.400 m;</li>
<li>Cu paraşuta deschisă, zburaţi paraşuta la 90 de grade faţă de pistă până când următorul grup a deschis.</li>
</ul>

<h4 class="clickable-heading">ZBORUL CU PARAȘUTA</h4>

<ul style="display:none">
<li>Respectaţi zonele de pierdere a înălţimii şi de pregătire a aterizării (schema de pilotare) marcate pe plan. Vă rugăm să nu faceţi conuri (viraje repetate de 360 de grade);</li>
<li>Nu sunt acceptate manevre bruşte/viraje agresive în zona desemnată pentru schema de pilotare;</li>
<li>Cu paraşuta deschisă vă rugăm să nu treceţi pista sub înălţimea de 500 m. Zburaţi tot timpul în lateralul ei;</li>
<li>Nu zburaţi şi nu aterizaţi niciodată pe pistă;</li>
<li>Se va respecta traficul şi schema de pilotare evitând abordajele. În schema de pilotare nu se va trece niciodată peste pistă;</li>
<li>Dacă nu sunteţi experimentaţi şi nu vă simţiţi confortabil cu traficul aerian, alegeţi o altă zonă de aterizare neobstaculată în apropierea aerodromului;</li>
<li>Aterizaţi tot timpul în direcţia indicată de săgeată;</li>
<li>În cazul în care vântul se schimbă pe parcursul aterizărilor continuaţi să aterizaţi în direcţia stabilită de săgeată;</li>
</ul>
