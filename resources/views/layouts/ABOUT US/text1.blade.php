  <h2 class="">CUM AM APARUT?</h2>    
<p>Aeroclubul Teritorial Baia Mare, asociaţie sportivă adusă la viaţă din pasiunea pentru parasutism,
 mentinuta si crescuta cu dragostea pentru zbor a membrilor sai. Cea mai mare asociatie de parasutism
  din Romania si una dintre cele mai mari din SE Europei, iti deschide portile in fiecare an
   din Aprilie pana in Noiembrie.<p>

<p></p>Cu vreme buna, un avion performant, numeroase facilitati si o altitudine de 4000m, 
dedicam toata activitatea noastra pentru dezvoltarea parasutismului in Romania,
 si promovarea parasutismului romanesc in afara granitelor tarii, asigurand multa distractie 
 si salturi memorabile pentru parasutistii de toate nivelurile de calificare. </p>
