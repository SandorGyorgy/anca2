  <h2 class="">CE ÎNSEAMNĂ UN SALT ÎN TANDEM?</h2>    
<p>Saltul în tandem este cea mai simplă și cea mai bună alternativă pentru cei care
 vor să experimenteze căderea liberă și zborul cu parașuta. Oricine poate face acest tip de salt 
 indiferent de varstă sau condiție fizică deoarece saltul în tandem se face cu un instructor
 experimentat.</p>

<p>Saltul în tandem se face cu o parașută specială pentru 2 persoane. 
Hamul pasagerului este atașat de hamul instructorului care are parașuta de tandem. Deoarece 
instructorul de tandem este responsabil pentru deschiderea parașutei și aterizarea
în siguranță, pregatirea pentru salt durează doar 15 min.</p>
